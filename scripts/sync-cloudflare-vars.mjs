const BLOCK_START = '  // BEGIN MANAGED VARS - generated by scripts/sync-cloudflare-vars.mjs';
const BLOCK_END = '  // END MANAGED VARS';

const [, , envFileArg = 'env.cloudflare', ...flags] = process.argv;
const dryRun = flags.includes('--dry-run');
const envValues = parseEnvFile(envFileArg);
const wranglerPath = resolve(process.cwd(), 'wrangler.jsonc');
const wranglerConfig = readFileSync(wranglerPath, 'utf8');

const selectedEntries = VAR_MAPPINGS
  .map(({ target, source, fallback }) => {
    const value = envValues[source] ?? (fallback ? envValues[fallback] : undefined);
    return [target, value];
  })
  .filter(([, value]) => typeof value === 'string' && value.length > 0)
  .sort(([left], [right]) => left.localeCompare(right));

if (selectedEntries.length === 0) {
  console.error(`No non-secret vars were found in ${envFileArg} for the allowlist.`);
  process.exit(1);
}

if (dryRun) {
  console.log(`Dry run: would sync ${selectedEntries.length} Wrangler vars from ${envFileArg}`);
  for (const [key, value] of selectedEntries) {
    console.log(`- ${key}=${value}`);
  }
  process.exit(0);
}

const block = `${BLOCK_START}\n  "vars": ${JSON.stringify(Object.fromEntries(selectedEntries), null, 2).replace(/^/gm, '  ')},\n${BLOCK_END}`;
let nextConfig;

if (wranglerConfig.includes(BLOCK_START) && wranglerConfig.includes(BLOCK_END)) {
  const pattern = new RegExp(`${escapeForRegExp(BLOCK_START)}[\\s\\S]*?${escapeForRegExp(BLOCK_END)}`);
  nextConfig = wranglerConfig.replace(pattern, block);
} else if (wranglerConfig.includes('  "assets": {')) {
  nextConfig = wranglerConfig.replace('  "assets": {', `${block}\n  "assets": {`);
} else {
  console.error('Could not find an insertion point for managed vars in wrangler.jsonc');
  process.exit(1);
}

writeFileSync(wranglerPath, nextConfig);

console.log(`Synced ${selectedEntries.length} managed vars into wrangler.jsonc from ${envFileArg}`);

function escapeForRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}